# sum()
# 封装一个函数，可以实现两个数相加，并得到和的结果
def my_sum(a, b):
    # print(a, b)
    s = a + b  # 功能：求和
    return s  # 返回结果

# 调用
res = my_sum(10, 20)  # a=10, b=20
print(res)  # 30
print(my_sum(10, 20))  # 30


