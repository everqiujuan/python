
# 栈 ：先进后出，后进先出

# 使用列表
# 列表list：
#   append() : 压栈，入栈，在列表的末尾追加元素
#   pop() : 出栈，在列表的末尾弹出元素

# 创建栈(空列表)
stack = []

# 入栈
stack.append('A')
print(stack)

stack.append('B')
print(stack)

stack.append('C')
print(stack)


# 出栈
stack.pop()
print(stack)

stack.pop()
print(stack)

stack.pop()
print(stack)


